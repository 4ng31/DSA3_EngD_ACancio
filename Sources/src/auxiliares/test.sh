cutfiles () {
	array=( "${1//,/ }" "${2//,/ }" )
	scan=$3
#	echo $3
	stype=$4
#	echo $4
	OUTPATH="/home/taller-dis1/Angel/ESA/testing/Archivos2"
	flag=0
	for i in "${array[@]}"; do   # The quotes are necessary here
		let flag=$flag+1
#		echo $flag
		tmparray=(${i})
		in_file=${tmparray[3]}
		out_file=${tmparray[3]}
	        out_file=$OUTPATH/${out_file##*/}.bin
		start=${tmparray[4]}
		end=${tmparray[5]}
		block_size=1468
		copy_size=$(( $end - $start ))
	#	echo "in_file=${tmparray[3]}
	#        out_file=$out_file
	#	start=${tmparray[4]}
	#        end=${tmparray[5]}
	#        block_size=1468
	#        copy_size=$(( $end - $start ))"	
		dd if="$in_file" of="$out_file" iflag=skip_bytes,count_bytes,fullblock bs="$block_size" skip="$start" count="$copy_size"
		
		if [ $flag == 1 ];then
			first=$out_file
		fi
		if [ $flag == 2 ];then
			second=$out_file
			flag=0
		fi
	done
	
#	echo "First $first"
#	echo "Second $second"
	outputfile=${second%_*}"_"${stype}"_"${scan}"_1934-638.bin"
	cat $first $second > $outputfile
}


